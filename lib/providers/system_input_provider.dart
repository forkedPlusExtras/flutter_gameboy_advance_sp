abstract class SystemInputProvider {
  // Shoulder buttons
  void onShoulderLeftTap() {}
  void onShoulderRightTap() {}

  // D-pad buttons
  void onDirectionalLeftTap() {}
  void onDirectionalRightTap() {}
  void onDirectionalUpTap() {}
  void onDirectionalDownTap() {}

  // Action buttons
  void onActionATap() {}
  void onActionBTap() {}

  // Other buttons
  void onMenuTap() {}
  void onSelectTap() {}
  void onStartTap() {}
}
